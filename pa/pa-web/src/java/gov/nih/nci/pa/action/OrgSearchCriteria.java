package gov.nih.nci.pa.action;

/**
 * 
 * @author Harsha
 *
 */
public class OrgSearchCriteria {
    private String orgName;
    private String nciOrgNumber;
    private String orgCountry;
    private String orgCity;
    private String orgZip;
    /**
     * @return the orgName
     */
    public String getOrgName() {
        return orgName;
    }
    /**
     * @param orgName the orgName to set
     */
    public void setOrgName(String orgName) {
        this.orgName = orgName;
    }
    /**
     * @return the nciOrgNumber
     */
    public String getNciOrgNumber() {
        return nciOrgNumber;
    }
    /**
     * @param nciOrgNumber the nciOrgNumber to set
     */
    public void setNciOrgNumber(String nciOrgNumber) {
        this.nciOrgNumber = nciOrgNumber;
    }
    /**
     * @return the orgCountry
     */
    public String getOrgCountry() {
        return orgCountry;
    }
    /**
     * @param orgCountry the orgCountry to set
     */
    public void setOrgCountry(String orgCountry) {
        this.orgCountry = orgCountry;
    }
    /**
     * @return the orgCity
     */
    public String getOrgCity() {
        return orgCity;
    }
    /**
     * @param orgCity the orgCity to set
     */
    public void setOrgCity(String orgCity) {
        this.orgCity = orgCity;
    }
    /**
     * @return the orgZip
     */
    public String getOrgZip() {
        return orgZip;
    }
    /**
     * @param orgZip the orgZip to set
     */
    public void setOrgZip(String orgZip) {
        this.orgZip = orgZip;
    }
    
    
    
}
